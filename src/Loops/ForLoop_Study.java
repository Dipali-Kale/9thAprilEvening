package Loops;

public class ForLoop_Study {

	public static void main(String[] args) 
	{
		//printing prime number
		System.out.println("----------------------------");
		System.out.println("1--->100");
		System.out.println("----------------------------");
		
		for(int i=1;i<=100;i=i+2)
		{
			System.out.print(i+" ");
		}
		

	}

}
